COLUMN TABLE_NAME FORMAT A20
COLUMN COLUMN_NAME FORMAT A19
COLUMN CONSTRAINT_NAME FORMAT A20
COLUMN R_CONSTRAINT_NAME FORMAT A20


SELECT A.TABLE_NAME, A.COLUMN_NAME, A.CONSTRAINT_NAME , B.CONSTRAINT_TYPE, 
       B.R_CONSTRAINT_NAME  , C.CONSTRAINT_TYPE , C.TABLE_NAME
FROM USER_CONS_COLUMNS A , USER_CONSTRAINTS B, USER_CONSTRAINTS C
WHERE B.r_constraint_name IN 
	(SELECT constraint_name FROM USER_CONSTRAINTS
	WHERE table_name='&tabela' AND constraint_type IN ('P','U'))
AND A.CONSTRAINT_NAME = B.CONSTRAINT_NAME
AND B.CONSTRAINT_TYPE='R'
AND B.R_CONSTRAINT_NAME = C.CONSTRAINT_NAME
/
